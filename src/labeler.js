const GLYPH_W = 5
const GLYPH_H = 7

const textWidth = (text, scale) => text.length * (GLYPH_W + 1) * scale

const drawText = (img, x, y, text, scale) => {
  const upper = text.toUpperCase()
  let cx = x
  for (const ch of upper) {
    const glyph = GLYPHS[ch] ?? GLYPHS[' ']
    for (let row = 0; row < GLYPH_H; row++) {
      const bits = glyph[row]
      for (let col = 0; col < GLYPH_W; col++) {
        if ((bits >> (GLYPH_W - 1 - col)) & 1) {
          for (let sy = 0; sy < scale; sy++) {
            for (let sx = 0; sx < scale; sx++) {
              const px = cx + col * scale + sx
              const py = y + row * scale + sy
              if (px >= 0 && py >= 0 && px < img.width && py < img.height) {
                const i = (py * img.width + px) * 4
                img.data[i] = 255
                img.data[i + 1] = 255
                img.data[i + 2] = 255
                img.data[i + 3] = 255
              }
            }
          }
        }
      }
    }
    cx += (GLYPH_W + 1) * scale
  }
}

const drawBackground = (img, x, y, w, h) => {
  for (let py = Math.max(0, y); py < Math.min(img.height, y + h); py++) {
    for (let px = Math.max(0, x); px < Math.min(img.width, x + w); px++) {
      const i = (py * img.width + px) * 4
      img.data[i]     = Math.round(img.data[i]     * 0.35)
      img.data[i + 1] = Math.round(img.data[i + 1] * 0.35)
      img.data[i + 2] = Math.round(img.data[i + 2] * 0.35)
    }
  }
}

/**
 * Draw text labels onto a rendered PNG buffer.
 * Each label is positioned above its centroid point.
 *
 * @param {Buffer} pngBuffer
 * @param {Array<{x: number, y: number, text: string}>} labels
 * @param {number} [scale=2] - pixel scale (2 = 10×14px per char)
 * @returns {Buffer}
 */
const labelPng = (pngBuffer, labels, scale = 2) => {
  const img = PNG.sync.read(pngBuffer)

  const padX = 3 * scale
  const padY = 2 * scale
  const textH = GLYPH_H * scale

  for (const { x, y, text } of labels) {
    const tw = textWidth(text, scale)
    const boxW = tw + padX * 2
    const boxH = textH + padY * 2

    // Place label above the centroid, horizontally centred, clamped to image
    const bx = Math.max(0, Math.min(img.width  - boxW, x - boxW / 2))
    const by = Math.max(0, Math.min(img.height - boxH, y - boxH - scale))

    drawBackground(img, bx, by, boxW, boxH)
    drawText(img, bx + padX, by + padY, text, scale)
  }

  return PNG.sync.write(img)
}

module.exports = { labelPng }
